# string slicing
a="Hello my name is rehan"
# 1. to print a specific index of the string
print(a[0:5])
# 2. to print string from end i.e, negative index
print(a[-5:])
# 3. print string without defining firs index value
print(a[:8])
# 4.print string by skiping some value
b="123456789"
print(b[::3])
# 5. to print string in reverse order
print(a[::-1])
print(b[::-1])
